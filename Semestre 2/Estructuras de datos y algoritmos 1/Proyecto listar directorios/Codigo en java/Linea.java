
/**
 * La clase linea crea un objeto de tipo linea, con los atributos posicion(pos) y profundidad(prof)
 * 
 * @author Santiago Avendaño y Samuel Cadavid 
 * @version noviembre 19 de 2017
 */
public class Linea
{
    public String texto;
    public int pos;
    public int prof;
    
    public Linea(String texto, int pos, int prof) {
        this.texto = texto;
        this.pos = pos;
        this.prof =  prof;
    }
    
}
